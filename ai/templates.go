// generateAttackBlock generates a single attack ChangeState controller.
func generateAttackBlock(config *AIConfig, stateID int, label, strategy, distCond, extraTrigger string, cooldownSet int, randThreshold int) string {
	var sb strings.Builder

	sb.WriteString(fmt.Sprintf("\n; --- %s (State %d) ---\n", label, stateID))
	sb.WriteString(fmt.Sprintf("[State -1, AI %s: %s]\n", strategy, label))
	sb.WriteString("type = ChangeState\n")
	sb.WriteString(fmt.Sprintf("value = %d\n", stateID))
	sb.WriteString(fmt.Sprintf("triggerall = %s\n", config.AIActivation))
	sb.WriteString("triggerall = RoundState = 2\n")
	sb.WriteString(fmt.Sprintf("triggerall = var(%d) = 0\n", config.CooldownVar)) // Anti-spam
	sb.WriteString(fmt.Sprintf("triggerall = var(%d) != %d\n", config.StateTrackVar, stateID)) // No repeat

	// Randomization gate
	if randThreshold > 0 {
		sb.WriteString(fmt.Sprintf("triggerall = Random < %d\n", randThreshold))
	}

	// Strategy condition
	switch strategy {
	case "PUNISH":
		sb.WriteString(fmt.Sprintf("triggerall = var(%d) = 2\n", config.StrategyVar))
	case "OFFENSE":
		sb.WriteString(fmt.Sprintf("triggerall = var(%d) = 0\n", config.StrategyVar))
	case "DEFENSE":
		sb.WriteString(fmt.Sprintf("triggerall = var(%d) = 1\n", config.StrategyVar))
	case "BAIT":
		sb.WriteString(fmt.Sprintf("triggerall = var(%d) = 3\n", config.StrategyVar))
	}

	// Distance condition
	if distCond != "" {
		sb.WriteString(fmt.Sprintf("triggerall = %s\n", distCond))
	}

	// Extra trigger conditions
	if extraTrigger != "" {
		sb.WriteString(fmt.Sprintf("triggerall = %s\n", extraTrigger))
	}

	sb.WriteString("trigger1 = ctrl\n")

	sb.WriteString("\n")

	// Set cooldown after using this move
	sb.WriteString(fmt.Sprintf("[State -1, AI Cooldown Set: %s]\n", label))
	sb.WriteString("type = VarSet\n")
	sb.WriteString(fmt.Sprintf("triggerall = %s\n", config.AIActivation))
	sb.WriteString(fmt.Sprintf("trigger1 = StateNo = %d\n", stateID))
	sb.WriteString("trigger1 = Time = 0\n")
	sb.WriteString(fmt.Sprintf("v = %d\n", config.CooldownVar))
	sb.WriteString(fmt.Sprintf("value = %d\n", cooldownSet))
	sb.WriteString("\n")

	// Track last used state
	sb.WriteString(fmt.Sprintf("[State -1, AI Track: %s]\n", label))
	sb.WriteString("type = VarSet\n")
	sb.WriteString(fmt.Sprintf("triggerall = %s\n", config.AIActivation))
	sb.WriteString(fmt.Sprintf("trigger1 = StateNo = %d\n", stateID))
	sb.WriteString("trigger1 = Time = 0\n")
	sb.WriteString(fmt.Sprintf("v = %d\n", config.StateTrackVar))
	sb.WriteString(fmt.Sprintf("value = %d\n", stateID))
	sb.WriteString("\n")

	return sb.String()
}

