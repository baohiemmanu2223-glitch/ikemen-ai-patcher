// enemyFunc returns the optimal enemynear string with target tracking if available.
func enemyFunc(config *AIConfig) string {
	if targetVar, ok := config.VarAllocation["TargetIndex"]; ok && targetVar > 0 {
		return fmt.Sprintf("enemynear(var(%d))", targetVar)
	}
	return "enemynear"
}

